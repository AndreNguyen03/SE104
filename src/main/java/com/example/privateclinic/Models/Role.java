package com.example.privateclinic.Models;

public class Role {
    int maQD;
    String tenQD;
    int giaTri;

    public Role() {
    }

    public int getMaQD() {
        return maQD;
    }

    public void setMaQD(int maQD) {
        this.maQD = maQD;
    }

    public String getTenQD() {
        return tenQD;
    }

    public void setTenQD(String tenQD) {
        this.tenQD = tenQD;
    }

    public int getGiaTri() {
        return giaTri;
    }

    public void setGiaTri(int giaTri) {
        this.giaTri = giaTri;
    }
}
